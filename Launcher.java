/**
 * Provides a standard entry point for launching the Donation Management System.
 * <p>
 * This class delegates application startup to the JavaFX {@code Main} class.
 */
public class Launcher {

    /**
     * Launches the application by forwarding command-line arguments to {@code Main}.
     *
     * @param args command-line arguments passed to the application
     */
    public static void main(String[] args) {
        Main.main(args);
    }
}